        // ends option scanning; later `-z`/`--zero` become operands.
        let flags = [long_bool('z', "zero")];
        let r = scan(&["foo", "-z", "--zero"], &flags, OnUnknown::Reject, Style::Gnu, false).unwrap();
        assert!(r.flags.is_empty());
